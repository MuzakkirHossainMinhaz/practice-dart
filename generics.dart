/**
 * Generics in Dart
 * - Generics are used to create reusable code
 * - Generics are enclosed in angle brackets <>
 */

void main() {
  // create a list
  List<int> numbers = [1, 2, 3];
  print(numbers);

  // create a set
  Set<String> names = {"Muzakkir", "Hossain", "Minhaz"};
  print(names);

  // create a map
  Map<int, String> numerics = {1: "One", 2: "Two", 3: "Three"};
  print(numerics);
}
